//Objeto MATH -> Possui instruções matemáticas

//ABS retornar valor absoluto ou seja sempre positivo
console.log(Math.abs(-9))

//Ceil arredonda para cima
console.log(Math.ceil(6.3))

//Floor arredonda para baixo
console.log(Math.floor(6.3))

//Pow potenciação primeiro numero = base, segundo numero = expoente
console.log(Math.pow(6, 3))

//Round arredonda <5 para cima e >5 para baixo
console.log(Math.round(5.3))

//Sqrt raiz quadrada
console.log(Math.sqrt(16))